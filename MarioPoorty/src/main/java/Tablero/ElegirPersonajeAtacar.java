	private void ponerEnemigosEnArrayList(Personaje personaAtacando, ArrayList<Personaje> personajes){
		contrincantes.add(null);
		for (Personaje personajeEnArrayList : personajes) {
			if(!personajeEnArrayList.equals(personaAtacando)){
				contrincantes.add(personajeEnArrayList);
			}
		}
	}
	private void ponerPersonajesEnCbx(){
		
		for (Personaje contrincante : contrincantes) {
			if(contrincante == null){
				cbxContrincantes.addItem("Elija Personaje");
				continue;
			}
			cbxContrincantes.addItem(contrincante.getNombre());
		}
	}

	public Personaje getEnemigoElegido() {
		return contrincantes.get(cbxContrincantes.getSelectedIndex());
	}
	
	
	
