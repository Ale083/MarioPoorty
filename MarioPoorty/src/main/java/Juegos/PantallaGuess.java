	private void ponerCeldas(){
		for (int i = 0; i < 10; i++) {
			for (int j = 0; j < 10; j++) {
				JButton boton = new JButton(preguntaIcon);
				txpPersonaje.add(boton);
				boton.setBounds(10+52*i,10+52*j,50,50);
				
				boton.addMouseListener(new MouseAdapter() {
                public void mouseClicked(MouseEvent evt) {
                    ponerInvisible(evt);
                }
                });
			}
		}
	}
	
