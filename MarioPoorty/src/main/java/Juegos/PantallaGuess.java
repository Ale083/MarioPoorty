	private void ponerInvisible(MouseEvent evento){
		if(celdasAQuitar == 0){
			return;
		}
		JButton boton = (JButton)evento.getComponent();
		boton.setIcon(null);
		boton.setOpaque(false);
		boton.setContentAreaFilled(false);
		boton.setBorderPainted(false);
		celdasAQuitar-=1;
		lblIntentos.setText("Intentos: " + celdasAQuitar);
	}

