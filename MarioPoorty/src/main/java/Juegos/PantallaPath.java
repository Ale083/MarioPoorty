	@Override
	public void actionPerformed(ActionEvent evt) {
		JButton boton =(JButton) evt.getSource();
		if(boton.getRolloverIcon()!=null){
            
            if(filasFaltantes == -1){
                JOptionPane.showInternalMessageDialog(null, "HAS GANADO");
                jugando = false;
				this.setVisible(false);
				perdio = false;
                return;
            }
            
            enableBotones();
            return;
        }
		if(intentos !=1){
			filasFaltantes = 5;
			enableBotones();
			intentos--;
			return;
		} else{
			JOptionPane.showInternalMessageDialog(null, "HAS PERDIDO");
            jugando = false;
			this.setVisible(false);
			perdio = true;
            return;
		}
	}

	public boolean isJugando() {
		return jugando;
	}
	
	public boolean isPerdio() {
		return perdio;
	}
	
