	private void ponerBotones(){
		for (int i = 0; i < 3; i++) {
			for (int j = 0; j < 6; j++) {
				JButton boton = new JButton(casillaIcon);
				casillas[i][j] = boton;
				pnlJuego.add(boton);
				boton.setBounds(i*55,j*55,50,50);
				boton.addActionListener(this);
				boton.setEnabled(true);
			}
		}
	}
	
	private void ponerCorrectos(){
		for (int fila = 0; fila < 6; fila++) {
			int num = Random.randomInt(0, 2);
			for (int col = 0; col < 3; col++) {
				if(num == col){
					casillas[col][fila].setRolloverIcon(checkIcon);
					break;
				}
			}
		}
	}
	
	private void enableBotones(){
		for (int i = 0; i < 6; i++) {
			for (int j = 0; j < 3; j++) {
				casillas[j][i].setEnabled(false);
				if(i == filasFaltantes){
					casillas[j][i].setEnabled(true);
				}
			}
		}
		filasFaltantes--;
	}

