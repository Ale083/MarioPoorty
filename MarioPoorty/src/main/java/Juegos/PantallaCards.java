	@Override
	public void actionPerformed(ActionEvent e) {
		JButton boton = (JButton)e.getSource();
		for (int i = 0; i < 4; i++) {
			for (int j = 0; j < 13; j++) {
				if(boton == botones[j][i]){
					cartaType = i;
					cartaNum = j;
					JOptionPane.showMessageDialog(this, toStringNum(cartaNum) + " de " + toStringType(cartaType), "Escogida", 1);
					this.setVisible(false);
					return;
				}
			}
		}
	}
