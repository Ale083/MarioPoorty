	private void ponerBotones(){
		for (int i = 0; i < 13; i++) {
			for (int j = 0; j < 4; j++) {
				botones[i][j] = new JButton(cartaIcon);
				JButton boton = botones[i][j];
				pnlCards.add(boton);
				boton.setBounds(20+38*i,20+58*j,33,53);
				boton.addActionListener(this);
			}
		}
	}
