	String toStringType(int num) {
		switch(num) {
			case 0: 
				return "Diamantes";
			case 1: 
				return "Espadas";
			case 2: 
				return "Trebloes";

			case 3: 
				return "Corazones";
			default:
				return "";
		}
	}

