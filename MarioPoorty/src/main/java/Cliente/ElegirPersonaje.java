	private void ponerVisiblesPersonajes(){ //pone visibles a los personajes existentes
		for (Integer disponible : disponibles) {
			switch (disponible) {
				case 0:
					btn0Mario.setVisible(true);
					break;
				case 1:
					btn1MrL.setVisible(true);
					break;
				case 2:
					btn2Peach.setVisible(true);
					break;
				case 3:
					btn3Toad.setVisible(true);
					break;
				case 4:
					btn4Yoshi.setVisible(true);
					break;
				case 5:
					btn5Luma.setVisible(true);
					break;
				case 6:
					btn6Bowser.setVisible(true);
					break;
				case 7:
					btn7Daisy.setVisible(true);
					break;
				case 8:
					btn8Rosalina.setVisible(true);
					break;
				case 9:
					btn9Dimentio.setVisible(true);
					break;
				default:
					System.out.println("Invalid option");
					break;
			}
		}
	}
	
